class Solution {
  public:
    string removePair(string s) {
        string ans = "";
        for(auto e : s)
        {
            if(ans.size() && e == ans.back()) ans.pop_back();
            else ans += e;
        }
        if(ans.size()) return ans;
        return "-1";
    }
};


