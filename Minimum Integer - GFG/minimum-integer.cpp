#include<bits/stdc++.h>
using namespace std;


class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends
class Solution {
  public:
    using ll = long long;
    int minimumInteger(int N, vector<int> &A) {
        ll ans = INT_MAX, sum = 0;
        for(int &e : A) sum += e;
        
        for(int &e : A)
        {
            if(e*1LL*N >= sum) ans = min(ans, (ll)e);
        }
        
        return (int) ans;
    }
};


